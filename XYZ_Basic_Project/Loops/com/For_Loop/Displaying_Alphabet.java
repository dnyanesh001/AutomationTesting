package com.For_Loop;

public class Displaying_Alphabet {
	public static void main(String[] args)
	{
		System.out.println("Write Program for display the Capital Alphabets");
		for(char ch='A';ch<='Z';ch++)
		{
			System.out.print(ch+"  ");
		}
		System.out.println("\nWrite Program for displaying the small alphabet");
		for(char ch='a';ch<='z';ch++)
		{
			System.out.print(ch+"  ");
		}
	}

}

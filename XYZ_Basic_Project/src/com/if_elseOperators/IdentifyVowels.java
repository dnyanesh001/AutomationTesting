/**Check whether an alphabet is vowel or consonant using if..else statement*/
package com.if_elseOperators;

public class IdentifyVowels {
	public static void main(String[] args) {
		System.out.println("welcome to World of alphabets");
		char alpha;
		alpha='n';
		if(alpha=='a')
			System.out.println("the given alphabet is vowel ");
		else if(alpha=='e')
			System.out.println("the given alphabet is vowel");
		else if(alpha=='i')
			System.out.println("the given alphabet is vowel ");
		else if(alpha=='o')
			System.out.println("the given alphabet is vowel ");
		else if(alpha=='u')
			System.out.println("the given alphabet is vowel ");
		else
			System.out.println("the given alphabet is consonanat");
	}

}
